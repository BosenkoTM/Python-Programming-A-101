# 3. ГРАФИЧЕСКАЯ ДИАГРАММА КЛАССОВ В СТИЛЕ UML НА matplotlib
# =====================================================================

fig, ax = plt.subplots(figsize=(14, 9))
ax.set_xlim(0, 14)
ax.set_ylim(0, 10)
ax.axis("off")
ax.set_title("Диаграмма классов модели «Линейка тарифов оператора связи»\n"
             "(Вариант 30, самостоятельное задание по ООП)",
             fontsize=13, fontweight="bold", pad=15)


def draw_class_box(ax, x, y, w, h, name, fields, methods, color):
    """
    Рисует UML-блок класса: 3 секции — имя, поля, методы.
    Координаты (x, y) — левый верхний угол.
    """
    # Цвета для трёх секций: заголовок насыщеннее, секции светлее.
    header_h = 0.5
    fields_h = 0.18 * len(fields) + 0.15
    methods_h = h - header_h - fields_h

    # Заголовок
    ax.add_patch(patches.Rectangle((x, y - header_h), w, header_h,
                                    facecolor=color, edgecolor="black",
                                    linewidth=1.5))
    ax.text(x + w / 2, y - header_h / 2, name,
            ha="center", va="center",
            fontsize=12, fontweight="bold", color="white")

    # Секция полей
    ax.add_patch(patches.Rectangle((x, y - header_h - fields_h),
                                    w, fields_h,
                                    facecolor="#fafafa", edgecolor="black",
                                    linewidth=1.2))
    for i, f in enumerate(fields):
        ax.text(x + 0.1, y - header_h - 0.18 - i * 0.18,
                f"− {f}", ha="left", va="top", fontsize=9,
                family="monospace")

    # Секция методов
    ax.add_patch(patches.Rectangle((x, y - header_h - fields_h - methods_h),
                                    w, methods_h,
                                    facecolor="#ffffff", edgecolor="black",
                                    linewidth=1.2))
    for i, m in enumerate(methods):
        ax.text(x + 0.1, y - header_h - fields_h - 0.18 - i * 0.18,
                f"+ {m}", ha="left", va="top", fontsize=9,
                family="monospace")


# Цвета для разных ролей (мягкая палитра).
color_base = "#3a7ca5"        # синий — базовый
color_derived = "#16a085"     # бирюзовый — наследник
color_container = "#c0392b"   # тёмно-красный — контейнер

# --- Базовый класс Tariff (сверху по центру) ---
draw_class_box(
    ax, x=4.5, y=9.7, w=5, h=3.8,
    name="Tariff   ← базовый класс",
    fields=["_name : str",
            "_monthly_fee : float",
            "_minutes : int",
            "_gigabytes : float"],
    methods=["@property name / monthly_fee / ...",
             "effective_fee()  → monthly_fee",
             "kind()  → 'обычный'",
             "__str__()"],
    color=color_base,
)

# --- Наследник FamilyTariff (снизу слева) ---
draw_class_box(
    ax, x=0.7, y=4.6, w=5.5, h=3.6,
    name="FamilyTariff(Tariff)   ← наследник",
    fields=["(+ всё из Tariff)",
            "_numbers_count : int (≥2)",
            "_discount : float [0..100]"],
    methods=["effective_fee()  ★ переопределён",
             "    = fee × N × (1 − d/100)",
             "kind()  → 'семейный'",
             "__str__()  ← переопределён"],
    color=color_derived,
)

# --- Контейнер Operator (снизу справа) ---
draw_class_box(
    ax, x=7.8, y=4.6, w=5.7, h=3.6,
    name="Operator   ← контейнер",
    fields=["_tariffs : list[Tariff]"],
    methods=["add_tariff(t)",
             "remove_tariff(name)  → Tariff",
             "find_tariff(name)    → Tariff | None",
             "affordable(max_fee)  → list",
             "sorted_by_price()    → list",
             "summary()            → dict"],
    color=color_container,
)

# --- Стрелка наследования: от FamilyTariff к Tariff ---
# В UML стрелка наследования — с пустым (белым) треугольником на конце.
ax.annotate(
    "", xy=(5.5, 9.2), xytext=(3.4, 4.6),
    arrowprops=dict(arrowstyle="-|>", linewidth=2,
                    color="black", mutation_scale=22,
                    connectionstyle="arc3,rad=0.0"),
)
ax.text(3.6, 7.5, "наследует\n(is-a)", fontsize=10,
        style="italic", color="#16a085", fontweight="bold")

# --- Связь агрегации: Operator содержит Tariff ---
# В UML агрегация — линия с ромбом со стороны контейнера.
ax.annotate(
    "", xy=(9.5, 9.2), xytext=(10.6, 4.6),
    arrowprops=dict(arrowstyle="-", linewidth=2, color="black",
                    connectionstyle="arc3,rad=0.0"),
)
